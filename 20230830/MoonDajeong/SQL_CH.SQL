#SQL(챌린지)_가장비싼식품
SELECT A.CATEGORY, A.PRICE, A.PRODUCT_NAME
FROM FOOD_PRODUCT AS A LEFT JOIN (SELECT CATEGORY, MAX(PRICE) AS MAX_PRICE
                                    FROM FOOD_PRODUCT GROUP BY CATEGORY) AS B
ON (A.CATEGORY=B.CATEGORY)
#JOIN 할때 ON으로 조건걸기!! WHERE 아님!
WHERE (A.CATEGORY IN ('과자','국','김치','식용유'))AND (A.PRICE=B.MAX_PRICE)
ORDER BY A.PRICE DESC;
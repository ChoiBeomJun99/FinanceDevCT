SELECT C.FLAVOR
FROM(
    SELECT A.FLAVOR,SUM(A.TOTAL_ORDER)+SUM(B.TOTAL_ORDER) AS TOTAL_ORDER
    FROM FIRST_HALF A
    JOIN JULY B ON A.FLAVOR=B.FLAVOR
    GROUP BY A.FLAVOR
    ORDER BY TOTAL_ORDER DESC
)C
WHERE ROWNUM<=3

/*1. GROUP BY에는 SELECT 절에 있는 놈을 써야 함
2. A+B=C(TOTAL)로 만들어야 함. 안그러면 여러개의 컬럼을 반환해 오류 발생
3. WHERE절과 GROUP BY 절에는 ALIAS 사용 불가능. ORDER BY 절에는 사용 가능 
*/
/* 
GROUP BY 절에는 ALIAS 사용X 
DISTINCT로 여러번 구매한 회원 중복제거********* 안 해서 틀림 
*/

SELECT EXTRACT(YEAR FROM B.SALES_DATE) AS YEAR, EXTRACT(MONTH FROM B.SALES_DATE) AS MONTH, A.GENDER, COUNT(DISTINCT A.USER_ID) AS USERS
FROM USER_INFO A 
JOIN ONLINE_SALE B ON A.USER_ID=B.USER_ID
WHERE A.GENDER IS NOT NULL
GROUP BY EXTRACT(YEAR FROM B.SALES_DATE), EXTRACT(MONTH FROM B.SALES_DATE), A.GENDER
ORDER BY YEAR ASC, MONTH ASC, GENDER ASC;
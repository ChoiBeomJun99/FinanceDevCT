/* 
1. REVIEW_DATE 포맷이 맞지 않아 오답처리 되는 부분. 
(MY SQL은 DATE_FORMAT을 쓰지만 오라클은 TO_CHAR)

2. 서브쿼리문을 작성하는 데 어려움 겪음...
*/

SELECT A.MEMBER_NAME, B.REVIEW_TEXT, TO_CHAR(B.REVIEW_DATE, 'YYYY-MM-DD') AS REVIEW_DATE
FROM MEMBER_PROFILE A
JOIN REST_REVIEW B ON A.MEMBER_ID = B.MEMBER_ID
WHERE A.MEMBER_ID = (
    SELECT MEMBER_ID
    FROM (
        SELECT MEMBER_ID, COUNT(*) AS REVIEW_COUNT
        FROM REST_REVIEW
        GROUP BY MEMBER_ID
        ORDER BY REVIEW_COUNT DESC
    )
    WHERE ROWNUM = 1
)
ORDER BY B.REVIEW_DATE ASC, B.REVIEW_TEXT ASC
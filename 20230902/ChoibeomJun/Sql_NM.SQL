SELECT BOARD_ID, WRITER_ID, TITLE, PRICE, IF(STATUS = 'SALE', '판매중' , IF(STATUS = 'RESERVED', '예약중', '거래완료')) AS STATUS 
FROM USED_GOODS_BOARD
WHERE EXTRACT(YEAR FROM CREATED_DATE) = 2022 AND EXTRACT(MONTH FROM CREATED_DATE) = 10 AND
EXTRACT(DAY FROM CREATED_DATE) = 5
ORDER BY BOARD_ID DESC
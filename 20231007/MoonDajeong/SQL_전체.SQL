SELECT A.CAR_ID, ROUND(AVG(A.RENTAL_DAYS),1) AS AVERAGE_DURATION
FROM (SELECT *, (TIMESTAMPDIFF(DAY,START_DATE,END_DATE)+1) AS 'RENTAL_DAYS'
      FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY) AS A
GROUP BY A.CAR_ID HAVING ROUND(AVG(A.RENTAL_DAYS),1) >= 7
ORDER BY AVERAGE_DURATION DESC, A.CAR_ID DESC;